package com.petro.smsapp

/**
 * یه سویچ ساده‌ی درون‌حافظه‌ای (in-memory، در سطح پروسس) که نشون میده کاربر همین الان
 * واقعاً داره کدوم thread رو می‌بینه - هم از نظر Navigation (روی صفحه‌ی چت همون مکالمه‌ست)
 * هم از نظر لایف‌سایکل (اپ رفته بک‌گراند نیست).
 *
 * SmsDeliverReceiver قبل از نمایش نوتیفِ پیام دریافتی این رو چک می‌کنه: اگه پیام مال
 * همین threadId بود، نوتیف/صدا لازم نیست چون کاربر داره زنده پیام رو روی صفحه می‌بینه
 * (ContentObserver خودش صفحه رو آپدیت می‌کنه).
 *
 * چون این یه Object سطح پروسسه، اگه پروسس اپ کشته بشه (مثلاً از حافظه پاک بشه) خودش با
 * مقدار پیش‌فرض null شروع میشه - یعنی هیچ‌وقت یه حالت "بازه"ی اشتباه از یه اجرای قبلی
 * باقی نمی‌مونه و نوتیف‌ها درست نشون داده میشن.
 */
object ActiveThreadTracker {
    @Volatile
    var activeThreadId: Long? = null
}
